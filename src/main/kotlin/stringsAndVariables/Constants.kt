package stringsAndVariables

const val COIN_MARKET_CAP_URL = "https://coinmarketcap.com/"

fun main() {

    // Constants are another way to declare a variable in Kotlin.

    // They are available at compile time and are not calculated during the program's execution.

    // Constants are top level meaning they are declared outside any functions or classes.

    // Names must be in all-caps.

    println(COIN_MARKET_CAP_URL)

}