package stringsAndVariables

fun main() {

    // Variable names can contain letters, numbers and number scores.
    // They cannot contain spaces.
    // They must start with a letter or an underscore.
    // Cannot match any Kotlin keywords.

    // For multiple word variable names Kotlin's standard syntax is camel case.
    val myNameIs = "Dylan Kerr"
    println(myNameIs)
    
}